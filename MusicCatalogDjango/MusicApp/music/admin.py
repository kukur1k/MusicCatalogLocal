from django.contrib import admin
from .models import Musician, Album, MusicTrack

# Регистрация модели Musician (Исполнитель)
@admin.register(Musician)
class MusicianAdmin(admin.ModelAdmin):
    """
    Настройки административного интерфейса для модели Musician.
    Определяет, как исполнители будут отображаться и управляться в админке.
    """
    
    # Поля, отображаемые в списке всех исполнителей
    list_display = ['name', 'country', 'start_year']
    # Колонки: Имя исполнителя | Страна | Начало карьеры
    
    # Поля, по которым можно выполнять поиск
    search_fields = ['name', 'country']
    # Поиск по имени исполнителя и стране
    
    # Поля для фильтрации в правой боковой панели
    list_filter = ['country', 'start_year']
    # Можно фильтровать исполнителей по стране и году начала карьеры


# Регистрация модели Album (Альбом)
@admin.register(Album)
class AlbumAdmin(admin.ModelAdmin):
    """
    Настройки административного интерфейса для модели Album.
    Определяет, как альбомы будут отображаться и управляться в админке.
    """
    
    # Поля, отображаемые в списке всех альбомов
    list_display = ['title', 'musician', 'release_date', 'genre']
    # Колонки: Название | Исполнитель | Дата выхода | Жанр
    
    # Поля для фильтрации в правой боковой панели
    list_filter = [
        'musician',        # Фильтр по исполнителю
        'release_date',    # Фильтр по дате выхода
        'genre',           # Фильтр по жанру
    ]
    
    # Поля, по которым можно выполнять поиск
    search_fields = [
        'title',                # Поиск по названию альбома
        'musician__name',       # Поиск по имени исполнителя (двойное подчеркивание для связей)
    ]
    
    # Иерархическая навигация по датам
    date_hierarchy = 'release_date'
    # Добавляет навигацию по годам/месяцам/дням над списком альбомов


# Регистрация модели MusicTrack (Трек)
@admin.register(MusicTrack)
class MusicTrackAdmin(admin.ModelAdmin):
    """
    Настройки административного интерфейса для модели MusicTrack.
    Определяет, как треки будут отображаться и управляться в админке.
    """
    
    # Поля, отображаемые в списке всех треков
    list_display = ['title', 'musician', 'album', 'release_date', 'genre', 'is_folove']
    # Колонки: Название | Исполнитель | Альбом | Дата выхода | Жанр | В избранном
    
    # Поля для фильтрации в правой боковой панели
    list_filter = [
        'is_folove',        # Фильтр по избранному
        'release_date',     # Фильтр по дате выхода
        'musician',         # Фильтр по исполнителю
        'album',            # Фильтр по альбому
        'genre',            # Фильтр по жанру
    ]
    
    # Поля, по которым можно выполнять поиск
    search_fields = [
        'title',                # Поиск по названию трека
        'musician__name',       # Поиск по имени исполнителя
        'album__title',         # Поиск по названию альбома
    ]
    
    # Иерархическая навигация по датам
    date_hierarchy = 'release_date'
    # Добавляет навигацию по годам/месяцам/дням над списком треков
    
    # Поля, которые можно редактировать прямо в списке
    list_editable = ['is_folove']  # Можно менять статус избранного прямо из списка